public class Move
{
	protected int startX;
	protected int endX;
	protected int startY;
	protected int endY;
	
	public int getStartX()
	{
		return this.startX;
	}
	
	public void setStartX(int startX)
	{
		this.startX = startX;
	}
	
	public int getEndX()
	{
		return this.endX;
	}
	
	public void setEndX(int endX)
	{
		this.endX = endX;
	}
	
	public int getStartY()
	{
		return this.startY;
	}
	
	public void setStartY(int startY)
	{
		this.startY = startY;
	}
	
	public int getEndY()
	{
		return this.endY;
	}
	
	public void setEndY(int endY)
	{
		this.endY = endY;
	}
	
}